<template>
  <div>
    <span>{{ count }}</span>
    <button type="button"
            @click="increment">+</button>
    <button type="button"
            @click="decrement">-</button>
  </div>
</template>

<script>
export default {
  data() {
    return {
      count: 0,
    }
  },
  methods: {
    increment() {
      this.count += 1;
    },
    decrement() {
      this.count -= 1;
    },
  },
};
</script>
